    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        String nome = cnome.getText();
        String endereco = cendereco.getText();
        String cpf = ccpf.getText();
        String telefone = ctelefone.getText();
        String tipo = (String) tipocliente.getSelectedItem();
        
        switch(tipo){
            case "Normal":
                Cliente c = new Cliente(nome, endereco, cpf, telefone, 0);
                padaria.clientes.add(c);
                break;
            case "Gold":
                Cliente g = new Cliente(nome, endereco, cpf, telefone, (float) 0.05);
                padaria.clientes.add(g);
                break;
            case "Platinum":
                Cliente p = new Cliente(nome, endereco, cpf, telefone, (float) 0.1);
                padaria.clientes.add(p);
                break;
        }
        JOptionPane.showMessageDialog(this, "Cadastro realizado com sucesso!");    
    }//GEN-LAST:event_cadastrarActionPerformed

