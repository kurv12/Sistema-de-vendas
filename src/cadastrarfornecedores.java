    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        String nome = cnome.getText();
        String endereco = cendereco.getText();
        String cnpj = ccnpj.getText();
        String desconto = cdesconto.getText();
        String ctipo = tipo.getSelectedItem() + "";
        
        if(ctipo.equals("Recorrente") ){
            FornecedorRecorrente f = new FornecedorRecorrente(nome, endereco, cnpj, Float.parseFloat(desconto));
            
        padaria.fornecedores.add(f);
        }else{
            Fornecedor f = new Fornecedor(nome, endereco, cnpj);  
            
        padaria.fornecedores.add(f);
        }
        JOptionPane.showMessageDialog(this, "Fornecedor Cadastrado com Sucesso");
    }//GEN-LAST:event_cadastrarActionPerformed

