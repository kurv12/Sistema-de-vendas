	// Calcula os impostos de cada funcionario
	protected static void calcularImpostoFuncionarios(Loja padaria) {
            for (Padeiro p : padaria.padeiros) {
                Imposto i = new Imposto("Funcionário", ((float) (p.calcularSalarioLiquido(p.getSalarioBruto())*0.15)));
                padaria.impostos.add(i);
            }
            for(Vendedor v: padaria.vendedores){
                Imposto i = new Imposto("Funcionário", ((float) (v.calcularSalarioLiquido(v.getSalarioBruto())*0.15)));
                padaria.impostos.add(i);
            }
            for(Gerente g: padaria.gerentes){
                Imposto i = new Imposto("Funcionário", ((float) (g.calcularSalarioLiquido(g.getSalarioBruto())*0.15)));
                System.out.println(i.getValor());
                padaria.impostos.add(i);
            }           
            
        }
	//Calcula o imposto sobre as vendas
	protected static void calcularImpostoVendas(Loja padaria) {
            for(Venda v: padaria.vendas){
                Imposto i = new Imposto("Venda",((float) v.getValorVenda()*0.18));
                padaria.impostos.add(i);
            }
	}
    
    
    
