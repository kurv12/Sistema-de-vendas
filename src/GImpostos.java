    //Popula jTable
    public void organizar(Loja padaria){
        String[] colunas = {"Tipo","Imposto"};
        DefaultTableModel modelo = new DefaultTableModel(colunas, 0);
        
        if(padaria.impostos.isEmpty()){
            modelo.addRow(new String[]{"Não há cadastro","Não há cadastro"});
            tabela.setModel(modelo);
        }else{
            for(Imposto i: padaria.impostos){
                modelo.addRow(new String[]{i.getTipo(), Float.toString(i.getValor())});
                tabela.setModel(modelo); 
            } 
        }
    }

